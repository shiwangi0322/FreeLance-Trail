import PDFDocument from "pdfkit";

// ---------------------------------------------------------------------------
// Design tokens — kept in one place so all three PDF types stay visually
// consistent, and a future brand color change is a one-line edit.
// ---------------------------------------------------------------------------
const COLORS = {
  brand: "#7C3AED",
  brandDark: "#5B21B6",
  textDark: "#1F2937",
  textGray: "#6B7280",
  textLight: "#9CA3AF",
  border: "#E5E7EB",
  tableHeaderBg: "#F5F3FF",
  success: "#059669",
  warning: "#D97706",
  danger: "#DC2626",
};

const PAGE_MARGIN = 50;
const PAGE_WIDTH = 595.28; // A4 width in points

function formatCurrency(amount) {
  return `Rs. ${Number(amount || 0).toLocaleString("en-IN", { minimumFractionDigits: 2, maximumFractionDigits: 2 })}`;
}

function formatDate(date) {
  if (!date) return "-";
  return new Date(date).toLocaleDateString("en-US", { day: "2-digit", month: "short", year: "numeric" });
}

// ---------------------------------------------------------------------------
// Shared layout pieces — header, footer, page numbers. Every PDF type calls
// these so a design change (e.g. swapping the logo placeholder for a real
// image later) only needs to happen in one place.
// ---------------------------------------------------------------------------

/**
 * Draws the branded header: logo placeholder + company name/tagline, plus
 * the document title (e.g. "INVOICE", "PROPOSAL", "SERVICE AGREEMENT") on
 * the right side of the same row.
 */
function drawHeader(doc, documentTitle) {
  // Logo placeholder — a simple rounded square with an "F" mark. Swap for
  // doc.image(path, x, y, { width }) once a real logo file is available.
  doc.roundedRect(PAGE_MARGIN, 45, 36, 36, 8).fill(COLORS.brand);
  doc
    .fillColor("#FFFFFF")
    .font("Helvetica-Bold")
    .fontSize(18)
    .text("F", PAGE_MARGIN + 11, 54);

  doc
    .fillColor(COLORS.textDark)
    .font("Helvetica-Bold")
    .fontSize(16)
    .text("Freedo", PAGE_MARGIN + 46, 50);

  doc
    .fillColor(COLORS.textGray)
    .font("Helvetica")
    .fontSize(9)
    .text("AI Freelancer Operating System", PAGE_MARGIN + 46, 70);

  // Document title, right-aligned
  doc
    .fillColor(COLORS.brand)
    .font("Helvetica-Bold")
    .fontSize(20)
    .text(documentTitle.toUpperCase(), PAGE_MARGIN, 52, {
      width: PAGE_WIDTH - PAGE_MARGIN * 2,
      align: "right",
    });

  doc
    .moveTo(PAGE_MARGIN, 96)
    .lineTo(PAGE_WIDTH - PAGE_MARGIN, 96)
    .strokeColor(COLORS.brand)
    .lineWidth(1.5)
    .stroke();

  return 112; // y-coordinate where the caller should continue drawing from
}

/**
 * Draws the footer + page number on the CURRENT page only. Call once per
 * page (see addFooterToAllPages for the multi-page case).
 */
function drawFooterOnCurrentPage(doc, pageNumber, totalPages) {
  const bottom = doc.page.height - 55;

  doc
    .moveTo(PAGE_MARGIN, bottom)
    .lineTo(PAGE_WIDTH - PAGE_MARGIN, bottom)
    .strokeColor(COLORS.border)
    .lineWidth(0.5)
    .stroke();

  doc
    .fillColor(COLORS.textLight)
    .font("Helvetica")
    .fontSize(8.5)
    .text("Generated by Freedo", PAGE_MARGIN, bottom + 10, { width: 250 });

  doc.text(`Page ${pageNumber} of ${totalPages}`, PAGE_MARGIN, bottom + 10, {
    width: PAGE_WIDTH - PAGE_MARGIN * 2,
    align: "right",
  });
}

/**
 * PDFKit only knows how many pages exist AFTER everything has been drawn
 * (since content can flow onto new pages automatically). So footers are
 * added in a final pass over doc.bufferedPageRange(), rather than while
 * content is being drawn — this is what correctly gives "Page 1 of 3"
 * instead of every page guessing wrong.
 */
function addFooterToAllPages(doc) {
  const range = doc.bufferedPageRange(); // { start, count }
  for (let i = 0; i < range.count; i++) {
    doc.switchToPage(range.start + i);
    drawFooterOnCurrentPage(doc, i + 1, range.count);
  }
}

function sectionHeading(doc, text, x, y) {
  doc.fillColor(COLORS.brand).font("Helvetica-Bold").fontSize(11).text(text.toUpperCase(), x, y);
  return y + 18;
}

function labelValue(doc, label, value, x, y, width = 240) {
  doc.fillColor(COLORS.textGray).font("Helvetica").fontSize(9).text(label, x, y);
  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(10).text(value || "-", x, y + 13, { width });
  return y + 34;
}

