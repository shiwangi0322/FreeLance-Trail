/**
 * `data` shape: { contract, project, client, freelancer }
 * `contract` fields are treated loosely (contract.terms, .scope,
 * .paymentTerms, .ownership, .termination) since the exact Contract model
 * shape isn't confirmed yet — pass whatever text fields you have; anything
 * missing falls back to a sensible default paragraph so the PDF never
 * breaks on an undefined field.
 */
export function generateContractPDF(doc, data) {
  const { contract = {}, project, client, freelancer } = data;

  let y = drawHeader(doc, "Service Agreement");

  doc
    .fillColor(COLORS.textDark)
    .font("Helvetica-Bold")
    .fontSize(13)
    .text(`Agreement for: ${project?.title || "-"}`, PAGE_MARGIN, y, { width: 495 });
  y += 30;

  const colY = y;
  y = sectionHeading(doc, "Client", PAGE_MARGIN, y);
  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(10).text(client?.name || "-", PAGE_MARGIN, y);
  doc.fillColor(COLORS.textGray).font("Helvetica").fontSize(9).text(client?.email || "-", PAGE_MARGIN, y + 14);

  let y2 = sectionHeading(doc, "Freelancer", 320, colY);
  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(10).text(freelancer?.name || "Freedo User", 320, y2);
  doc.fillColor(COLORS.textGray).font("Helvetica").fontSize(9).text(freelancer?.email || "-", 320, y2 + 14);
  y += 45;

  function textSection(title, content, fallback) {
    if (y > doc.page.height - 160) {
      doc.addPage();
      y = drawHeader(doc, "Service Agreement");
    }
    y = sectionHeading(doc, title, PAGE_MARGIN, y);
    doc.fillColor(COLORS.textDark).font("Helvetica").fontSize(9.5).text(content || fallback, PAGE_MARGIN, y, { width: 495 });
    y = doc.y + 16;
  }

  textSection("Scope of Work", contract.scope, `This agreement covers all work related to "${project?.title || "the project"}" as outlined between both parties.`);
  textSection("Terms", contract.terms, "Standard terms apply as agreed upon by both parties prior to project commencement.");
  textSection("Payment Terms", contract.paymentTerms, "Payment is due per the agreed invoicing schedule, within the terms specified on each invoice.");
  textSection("Ownership", contract.ownership, "Upon full payment, all final deliverables become the property of the client.");
  textSection("Termination", contract.termination, "Either party may terminate this agreement with written notice, subject to payment for work completed to date.");

  // Signature blocks
  if (y > doc.page.height - 140) {
    doc.addPage();
    y = drawHeader(doc, "Service Agreement");
  }
  y += 20;
  doc.moveTo(PAGE_MARGIN, y).lineTo(PAGE_MARGIN + 200, y).strokeColor(COLORS.border).stroke();
  doc.moveTo(320, y).lineTo(320 + 200, y).strokeColor(COLORS.border).stroke();
  doc.fillColor(COLORS.textGray).fontSize(8.5).text("Client Signature & Date", PAGE_MARGIN, y + 5);
  doc.text("Freelancer Signature & Date", 320, y + 5);
}

// ---------------------------------------------------------------------------
// Public entry point used by the controller — wraps drawing + footer pass +
// buffer collection in one place, so the controller doesn't repeat this
// boilerplate for all three document types.
// ---------------------------------------------------------------------------

/**
 * `type`: "invoice" | "proposal" | "contract"
 * Returns a Promise<Buffer> of the finished PDF.
 */
export function renderPdfToBuffer(type, data) {
  const drawFn = { invoice: generateInvoicePDF, proposal: generateProposalPDF, contract: generateContractPDF }[type];
  if (!drawFn) throw new Error(`Unknown PDF type: ${type}`);

  return new Promise((resolve, reject) => {
    const doc = new PDFDocument({ size: "A4", margin: PAGE_MARGIN, bufferPages: true });
    const chunks = [];

    doc.on("data", (chunk) => chunks.push(chunk));
    doc.on("end", () => resolve(Buffer.concat(chunks)));
    doc.on("error", reject);

    drawFn(doc, data);
    addFooterToAllPages(doc); // must run before doc.end() while pages are still buffered
    doc.end();
  });
}