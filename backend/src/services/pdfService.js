/**
 * `data` shape: { proposal, project, client }
 * `proposal.deliverables` and `proposal.technologies` are string arrays.
 */
export function generateProposalPDF(doc, data) {
  const { proposal, project, client } = data;

  let y = drawHeader(doc, "Proposal");

  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(14).text(project?.title || "Project Proposal", PAGE_MARGIN, y);
  y += 25;

  y = labelValue(doc, "PREPARED FOR", client?.name || "-", PAGE_MARGIN, y, 240);
  y -= 34;
  labelValue(doc, "STATUS", proposal.status, 350, y, 195);
  y += 34;

  y = sectionHeading(doc, "Introduction", PAGE_MARGIN, y);
  doc
    .fillColor(COLORS.textDark)
    .font("Helvetica")
    .fontSize(9.5)
    .text(
      proposal.overview ||
        `We're excited to present this proposal for ${project?.title || "your project"}, outlining our approach, deliverables, and timeline.`,
      PAGE_MARGIN,
      y,
      { width: 495 }
    );
  y = doc.y + 16;

  y = sectionHeading(doc, "Project Overview", PAGE_MARGIN, y);
  doc.fillColor(COLORS.textDark).font("Helvetica").fontSize(9.5).text(proposal.scope || "-", PAGE_MARGIN, y, { width: 495 });
  y = doc.y + 16;

  y = sectionHeading(doc, "Objectives", PAGE_MARGIN, y);
  doc.fillColor(COLORS.textDark).font("Helvetica").fontSize(9.5).text(proposal.objectives || "-", PAGE_MARGIN, y, { width: 495 });
  y = doc.y + 16;

  y = sectionHeading(doc, "Deliverables", PAGE_MARGIN, y);
  (proposal.deliverables || []).forEach((item) => {
    doc.fillColor(COLORS.textDark).font("Helvetica").fontSize(9.5).text(`•  ${item}`, PAGE_MARGIN + 5, y);
    y += 15;
  });
  y += 8;

  const colY = y;
  y = sectionHeading(doc, "Timeline", PAGE_MARGIN, y);
  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(10).text(`${proposal.timelineWeeks} weeks`, PAGE_MARGIN, y);

  let y3 = sectionHeading(doc, "Pricing", 320, colY);
  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(10).text(formatCurrency(proposal.budget), 320, y3);
  y += 40;

  // Acceptance section
  if (y > doc.page.height - 180) {
    doc.addPage();
    y = drawHeader(doc, "Proposal");
  }
  y = sectionHeading(doc, "Acceptance", PAGE_MARGIN, y);
  doc
    .fillColor(COLORS.textGray)
    .font("Helvetica")
    .fontSize(9)
    .text("By signing below, both parties agree to the terms outlined in this proposal.", PAGE_MARGIN, y, { width: 495 });
  y += 40;

  doc.moveTo(PAGE_MARGIN, y).lineTo(PAGE_MARGIN + 200, y).strokeColor(COLORS.border).stroke();
  doc.moveTo(320, y).lineTo(320 + 200, y).strokeColor(COLORS.border).stroke();
  doc.fillColor(COLORS.textGray).fontSize(8.5).text("Client Signature", PAGE_MARGIN, y + 5);
  doc.text("Freelancer Signature", 320, y + 5);
}

