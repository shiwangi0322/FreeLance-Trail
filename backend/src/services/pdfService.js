/**
 * `data` shape: { invoice, items, project, client }
 */
export function generateInvoicePDF(doc, data) {
  const { invoice, items = [], project, client } = data;

  let y = drawHeader(doc, "Invoice");

  // Invoice meta row: number/status on left, dates on right
  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(13).text(`# ${invoice.number}`, PAGE_MARGIN, y);

  const statusColor = { PAID: COLORS.success, PENDING: COLORS.warning, OVERDUE: COLORS.danger, DRAFT: COLORS.textGray };
  doc
    .fillColor(statusColor[invoice.status] || COLORS.textGray)
    .font("Helvetica-Bold")
    .fontSize(9)
    .text(invoice.status, PAGE_MARGIN, y + 18);

  doc.fillColor(COLORS.textGray).font("Helvetica").fontSize(9);
  doc.text(`Issue Date: ${formatDate(invoice.issueDate)}`, 350, y, { width: 195, align: "right" });
  doc.text(`Due Date: ${formatDate(invoice.dueDate)}`, 350, y + 14, { width: 195, align: "right" });
  y += 50;

  // Bill To / Project — two columns
  const colStartY = y;
  y = sectionHeading(doc, "Bill To", PAGE_MARGIN, y);
  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(10).text(client?.name || "-", PAGE_MARGIN, y);
  doc.fillColor(COLORS.textGray).font("Helvetica").fontSize(9).text(client?.email || "-", PAGE_MARGIN, y + 14);

  let y2 = sectionHeading(doc, "Project", 320, colStartY);
  doc.fillColor(COLORS.textDark).font("Helvetica-Bold").fontSize(10).text(project?.title || "-", 320, y2, { width: 225 });

  y += 45;

  // Items table
  const tableX = { item: PAGE_MARGIN, desc: 190, qty: 350, rate: 400, amount: 470 };
  doc.rect(PAGE_MARGIN, y, PAGE_WIDTH - PAGE_MARGIN * 2, 22).fill(COLORS.tableHeaderBg);
  doc.fillColor(COLORS.brandDark).font("Helvetica-Bold").fontSize(8.5);
  doc.text("ITEM", tableX.item + 8, y + 7);
  doc.text("DESCRIPTION", tableX.desc, y + 7);
  doc.text("QTY", tableX.qty, y + 7);
  doc.text("RATE", tableX.rate, y + 7);
  doc.text("AMOUNT", tableX.amount, y + 7);
  y += 22;

  let subtotal = 0;
  items.forEach((item, i) => {
    const qty = Number(item.qty);
    const rate = Number(item.rate);
    const amount = qty * rate;
    subtotal += amount;

    const rowHeight = 22;
    if (i % 2 === 1) doc.rect(PAGE_MARGIN, y, PAGE_WIDTH - PAGE_MARGIN * 2, rowHeight).fill("#FAFAFA");

    doc.fillColor(COLORS.textDark).font("Helvetica").fontSize(9);
    doc.text(item.name, tableX.item + 8, y + 6, { width: 130 });
    doc.fillColor(COLORS.textGray).text(item.description || "-", tableX.desc, y + 6, { width: 150 });
    doc.fillColor(COLORS.textDark).text(String(qty), tableX.qty, y + 6);
    doc.text(formatCurrency(rate), tableX.rate, y + 6);
    doc.font("Helvetica-Bold").text(formatCurrency(amount), tableX.amount, y + 6);

    y += rowHeight;

    // Manual page break if we're running out of room for the next row
    if (y > doc.page.height - 220) {
      doc.addPage();
      y = drawHeader(doc, "Invoice");
    }
  });

  y += 10;
  doc.moveTo(PAGE_MARGIN, y).lineTo(PAGE_WIDTH - PAGE_MARGIN, y).strokeColor(COLORS.border).stroke();
  y += 14;

  // Summary block, right-aligned
  const taxAmount = (subtotal * Number(invoice.taxRate)) / 100;
  const grandTotal = subtotal + taxAmount;
  const balanceDue = Math.max(0, grandTotal - Number(invoice.amountPaid));

  function summaryRow(label, value, bold = false, color = COLORS.textDark) {
    doc.fillColor(COLORS.textGray).font("Helvetica").fontSize(9.5).text(label, 350, y, { width: 100 });
    doc
      .fillColor(color)
      .font(bold ? "Helvetica-Bold" : "Helvetica")
      .fontSize(bold ? 11 : 9.5)
      .text(value, 450, y, { width: 95, align: "right" });
    y += 18;
  }

  summaryRow("Subtotal", formatCurrency(subtotal));
  summaryRow(`Tax (${invoice.taxRate}%)`, formatCurrency(taxAmount));
  summaryRow("Grand Total", formatCurrency(grandTotal), true);
  summaryRow("Amount Paid", formatCurrency(invoice.amountPaid));
  summaryRow("Balance Due", formatCurrency(balanceDue), true, COLORS.danger);
  y += 15;

  if (invoice.notes) {
    y = sectionHeading(doc, "Payment Notes", PAGE_MARGIN, y);
    doc.fillColor(COLORS.textDark).font("Helvetica").fontSize(9).text(invoice.notes, PAGE_MARGIN, y, { width: 495 });
  }
}

